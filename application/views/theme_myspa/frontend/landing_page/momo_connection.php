<script type="text/javascript">
// active button regis
window.addEventListener("scroll", function() {
    const scrolled = window.scrollY;
    const windowPosition = scrolled > 200;
    if (cartBottom) {
        cartBottom.classList.toggle("btn-goto__active", windowPosition);
        const scrollable = document.documentElement.scrollHeight - window.innerHeight;
        if (Math.ceil(scrolled) >= scrollable) {
            cartBottom.classList.remove("btn-goto__active");
        }
    }
});
// close button regis

// active popup regis
const closePopup = () => {
    $('.modal-open').css('position', 'unset');
}
const gotoRegis = () => {
    $('#popup_combo').modal('show');
    $('#popup_combo').css('display', 'flex');
    $('.modal-open').css('position', 'unset');

}
</script>

